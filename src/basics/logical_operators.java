package basics;

public class logical_operators {

	public static void main(String[] args) {
	
	    String username="abc";
	    String password="abcd3";
	    System.out.println(username=="abc" && password=="abcd");
	    System.out.println(username=="abc" || password=="abcd");
	    System.out.println(!(username=="abc"));
	    System.out.println(username=="abc");
	}

}
