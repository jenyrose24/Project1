package basics;

public class relational_operators {

	public static void main(String[] args) {
		int a=60;
	    int b=20;
		System.out.println(a>b);
		System.out.println(a<b);
	    System.out.println(a!=b);
	    System.out.println(a>=b);
	    System.out.println(a<=b);
	    System.out.println(a==b);
	}

}
