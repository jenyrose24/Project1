package Assignments;

public class sumof_naturalnumbers {

	public static void main(String[] args) {
		//to find the sum of natural numbers
		int sum=0;
	for(int i=0;i<=10;i++)
	{
	 sum += i;
	// System.out.println(sum);			//--------- to see each iteration results
	}
	System.out.println("The sum of natural numbers from 1 to 100 is "+sum);
	}

}
